package Module2.Lessons11;

 class Person {
     private String name;
     private int age;
     private String company;

     public void setName(String name) {
         this.name = name;
     }

     public void setCompany(String company) {
         this.company = company;
     }

     public int getAge() {
         return age;
     }

     private String getCompany() {
         return company;
     }
 }
